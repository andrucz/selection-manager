package com.andrucz.selectionmanager;

public final class Foo {

	private int bar;
	private String baz;
	
	public Foo(int bar, String baz) {
		this.bar = bar;
		this.baz = baz;
	}
	
	public Foo() {
	}
	
	public int getBar() {
		return bar;
	}
	
	public void setBar(int bar) {
		this.bar = bar;
	}
	
	public String getBaz() {
		return baz;
	}
	
	public void setBaz(String baz) {
		this.baz = baz;
	}
	
}
